// Em JavaScript, existem vários tipos de dados para representar diferentes tipos de informações.

// 1. String: Usado para representar texto.
const texto = "Isso é uma string";

// 2. Number: Representa valores numéricos (inteiros ou de ponto flutuante).
const numero = 42;

// 3. Boolean: Representa valores verdadeiros ou falsos.
const verdadeiro = true;
const falso = false;

// 4. Null: Representa a ausência intencional de valor.
const nulo = null;

// 5. Undefined: Indica que uma variável foi declarada, mas não atribuída a um valor.
let indefinido;

// 6. Object: É usado para criar objetos contendo pares de chave-valor.
const pessoa = { nome: "João", idade: 30 };

// 7. Array: Armazena uma lista ordenada de valores.
const lista = [1, 2, 3];

// 8. Function: Representa funções e permite que você execute código.
const funcao = function () {
  console.log("Isso é uma função.");
};

// 9. Symbol: Usado para criar valores únicos e é geralmente usado como propriedades de objetos.
const simbolo = Symbol("descrição");

// 10. BigInt: Introduzido no ES11 (ES2020), representa números inteiros maiores que o limite máximo de Number.
const numeroGrande = 1234567890123456789012345678901234567890n;

// 11. Date: Usado para representar datas e horários.
const data = new Date();

// 12. RegExp: Representa expressões regulares, que são padrões de pesquisa de texto.
const expressaoRegular = /abc/;

// 13. Map e Set: São estruturas de dados usadas para armazenar coleções de valores únicos (Set) ou pares de chave-valor (Map).
const conjunto = new Set();
const mapa = new Map();

// 14. Error: Usado para representar erros em JavaScript, normalmente lançado quando ocorre uma exceção.
const erro = new Error("Isso é um erro.");

// Estes são os tipos de dados fundamentais em JavaScript, cada um com seu propósito e características únicas.
